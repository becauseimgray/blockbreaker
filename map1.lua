function map1.draw(ball, rect1, list, screen)
  for rowIndex=1, #TileTable do
                local row = TileTable[rowIndex]
                for columnIndex=1, #row do
                  local number = row[columnIndex]
                  local x = (columnIndex-1)*TileWidth
                  local y = (rowIndex-1)   *TileHeight
                  local blockheight = 64
                  local blockwidth  = 25

                  if     number == 2 then
                    love.graphics.setColor(0, 0, 0, 0)
                      love.graphics.rectangle("fill", x, y, blockheight, blockwidth)
                    love.graphics.setColor(255,255,255,255)
                  elseif number == 1 then
                    love.graphics.setColor(255,255,255,255)
                    love.graphics.rectangle("fill",   x, y, blockheight, blockwidth)
                  end

                    --collision detection, if 1 then 2
                          if CheckCollision(x, y, blockwidth, blockheight, ball.x, ball.y, ball.w, ball.h) then
                              blockstring = "hit block number"..columnIndex..","..rowIndex..""
                                if (TileTable[rowIndex][columnIndex] ~= 2) then --if the block exists
                                  TileTable[rowIndex][columnIndex] = 2 -- make the block not exist
                                  points = points + 1
                                  if ball.y < y then
                                    ball.speedy = -math.abs(ball.speedy)
                                  elseif ball.y > y then
                                    ball.speedy =math.abs(ball.speedy)
                                  end
                                  TEsound.play(list)
                                end
                          end
                  end
  end
end
